function countProfit(shoppers) {
  let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                     ['Baju Zoro', 500000, 2],
                     ['Sweater Uniklooh', 175000, 1]
                   ];

  // you can only write your code here!
  let results = [];
  let keys = Object.keys(shoppers);

  if (keys.length === 0) {
    return results;
  }

  for (let i = 0; i < listBarang.length; i++) {
    var reports = {};

    reports.product = listBarang[i][0];
    reports.shoppers = [];
    reports.leftOver = listBarang[i][2];
    reports.totalProfit = 0;

    let remains = listBarang[i][2];
    
    for (let j = 0; j < keys.length; j++) {
      if (listBarang[i][0] === shoppers[j].product) {
        if (remains >= shoppers[j].amount) {
          reports.shoppers.push(shoppers[j].name);
          remains -= shoppers[j].amount;
          reports.leftOver = remains;
          reports.totalProfit = (listBarang[i][2] - remains) * listBarang[i][1];
        }
      }
    }
    results.push(reports);
  }

  return results;
}

